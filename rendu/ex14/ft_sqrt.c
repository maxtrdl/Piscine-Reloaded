int		ft_sqrt(int nb)
{
	int x;

	x = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (x * x < nb)
		x++;
	if (x * x != nb)
		return (0);
	return (x);
}
