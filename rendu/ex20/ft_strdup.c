#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	int		size_src;
	char	*dest;

	size_src = 0;
	i = 0;
	while (src[size_src])
		size_src++;
	dest = malloc(size_src * sizeof(char));
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
